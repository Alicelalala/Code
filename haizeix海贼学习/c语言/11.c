#include<stdio.h>
int main() {
    int i;
    int a[100] = {0};
    int j;
    a[0] = 1;

    for (i = 2; i* i <= 100; i++) {
        for (j = i; j <= 100 / i; i++) {
            a[i * j - 1] = 1;
        }
    }

    for (i = 0; i < 100; i++) {
        if (a[i] == 0) {
            printf("%d ", i + 1);
        }
    }
}
